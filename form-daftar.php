            var jenisKelamin = document.forms["daftarForm"]["jenis_kelamin"].value;
            var agama = document.forms["daftarForm"]["agama"].value;
            var sekolahAsal = document.forms["daftarForm"]["sekolah_asal"].value;

            if (nama == "" || alamat == "" || jenisKelamin == "" || agama == "" || sekolahAsal == "") {
